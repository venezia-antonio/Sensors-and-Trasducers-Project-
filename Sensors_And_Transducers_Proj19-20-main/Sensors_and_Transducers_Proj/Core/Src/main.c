void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	counter++;
	float buffer_mean = get_mean();
	raw_mean = buffer_mean/counter + ((float)(counter-1)/counter)*raw_mean;
}

double get_mean()
{
	uint32_t sum = 0;
	for (int i = 0; i < n_samples; i++)
	{
		sum += buffer_raw[i];
	}
	return (sum/n_samples);
}
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	/*Read data from Sensors*/
	float inputV = raw_mean * Vref / 4095;
	double current = (double)inputV / kT;
	double speed = get_speed();
	/*    CONTROLLER    */
	float P_speed = P_controller(speed);
	actuate_control_action(P_speed);
	/*Send data to MATLAB*/
	HAL_UART_Transmit(&huart2, (uint8_t*)&current,sizeof(current),n_samples);
	HAL_UART_Transmit(&huart2, (uint8_t*)&speed,sizeof(speed),n_samples);
	counter = 0;
	raw_mean = 0;
}
double get_speed()
{
	/* Calculate system states */
	count2_encoder = __HAL_TIM_GET_COUNTER(&htim3);
	double difference_count_motor = (double)difference_count(count2_encoder,count1_encoder,htim3);
	count1_encoder = count2_encoder;
	return (((double)difference_count_motor * 60)/(gear_ratio_motor * time_elapsed * CPR_encoder_motor)); // rpm
}
int32_t difference_count(uint32_t cnt2,uint32_t cnt1,TIM_HandleTypeDef htim)
{
	/* Function used to avoid count overflow */
	int32_t difference = cnt2 - cnt1;
	if (__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim) && (cnt2 > cnt1))
		{
			difference =  cnt2 - __HAL_TIM_GET_AUTORELOAD(&htim) - cnt1;
		}
	else if ((__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim) == 0) && (cnt1 > cnt2))
		{
			difference = - cnt1 + __HAL_TIM_GET_AUTORELOAD(&htim) + cnt2;
		}
	return difference;
}
void actuate_control_action(float P_term)
{
	/* Calculate duty cycle */
	v_out = v_out + P_term;
	float duty = v_out/Vm;
	/* Duty cycle saturation block */
	if (duty > 1)
	{
		duty = 1;
		v_out = Vm;
	}
	else if (duty < 0)
	{
		duty = 0;
		v_out = 0;
	}
	/* Set CCR in order to get the PWM signal */
	uint16_t ccr = (uint16_t)(fabs(duty)*(float)(1+__HAL_TIM_GET_AUTORELOAD(&htim4)));
	__HAL_TIM_SET_COMPARE(&htim4,TIM_CHANNEL_1,ccr);
}
float P_controller(float speed)
{
	/* P Controller */
	float proportional_error = set_point - speed;
	float P_term = Kp * proportional_error;
	return(P_term);
}
